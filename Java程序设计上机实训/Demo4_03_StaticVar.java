package Java程序设计上机实训;
public class Demo4_03_StaticVar {
    static int x;
    int y;
    public static int getX(){
        return x;
    }
    public static void setX(int newX){
        x=newX;
    }
    public int getY(){
        return y;
    }
    public void setY(int newY){
        y=newY;
    }
}
